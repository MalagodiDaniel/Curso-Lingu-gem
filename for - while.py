## A função `range()` em Python é usada para gerar uma sequência de números.
## É comumente utilizado em loops para iterar sobre uma série de valores 


pontos_de_vida = 0

for i in range(1, 11):
    pontos_de_vida += 1
    print("Tomou poção mágica" + str(i))

print(str(pontos_de_vida) + " Totais ")





##while##
i = 3

while i <= 3:
    print("olá")
    i += 1
    
